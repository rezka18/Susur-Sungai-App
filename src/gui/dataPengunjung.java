    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try{
            pengunjung p = new pengunjung();
            this.stat = k.getCon().prepareStatement("insert into pengunjung values(?,?,?,?,?)");
            this.stat.setString(1, p.id_pengunjung);
            this.stat.setString(2, p.nama);
            this.stat.setString(3, p.jenis_kelamin);
            this.stat.setString(4, p.telp);
            this.stat.setString(5, p.alamat);
            this.stat.executeUpdate();
            refreshTable();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        try{
            pengunjung p = new pengunjung();
            this.stat = k.getCon().prepareStatement("delete from pengunjung " 
                    + "where id_pengunjung=?");
            this.stat.setString(1, p.id_pengunjung);
            this.stat.executeUpdate();
            refreshTable();
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
            txtId.setText("");
            txtNama.setText("");
            txtAlamat.setText("");
            txtTelepon.setText("");
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try{
            pengunjung p = new pengunjung();
            this.stat = k.getCon().prepareStatement("update pengunjung set nama=?," 
                    + "jenis_kelamin=?,telp=?,alamat=?" 
                    + "where id_pengunjung=?");
            this.stat.setString(1, p.nama);
            this.stat.setString(2, p.jenis_kelamin);
            this.stat.setString(3, p.telp);
            this.stat.setString(4, p.alamat);
            this.stat.setString(5, p.id_pengunjung);
            this.stat.executeUpdate();
            refreshTable();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah");
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

