    public void refreshTable(){
        model = new DefaultTableModel();
        model.addColumn("ID Pengunjung");
        model.addColumn("Nama");
        model.addColumn("Jenis Kelamin");
        model.addColumn("Telepon");
        model.addColumn("Alamat");
        tblPengunjung.setModel(model);
        try{
            this.stat = k.getCon().prepareStatement("select * from pengunjung");
            this.rs = this.stat.executeQuery();
            while (rs.next()){
                Object[] data={
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5)
                };
                model.addRow(data);
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
